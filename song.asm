  DELAY PROC                
    PUSH AX               
    MOV AX,40H               
    MOV ES,AX                 
    MOV AX,[CLOCK]
    
    KETUKAWAL:
      CMP AX, [CLOCK]
      MOV CX, 4               
      JE KETUKAWAL
    
    LOOPDELAY:
      MOV AX, [CLOCK]
      KETUK:
        CMP AX,[CLOCK]
        JE KETUK
        LOOP LOOPDELAY
        POP AX
      RET
  DELAY ENDP   

  SOUNDER PROC
    PUSH AX
    IN AL, 61H
    OR AL, 00000011B          
    OUT 61H, AL 	            
    MOV AL, 0B6H
    OUT 43H, AL
    MOV AX, [TONE]            
    OUT 42H, AL               
    MOV AL, AH
    OUT 42H, AL               
    POP AX
    RET
  SOUNDER ENDP

  ;AGAR SUARANYA MATI
  MATISUARA PROC             
      IN AL,61H
      AND AL, 11111100B       
      OUT 61H, AL
      RET
  MATISUARA ENDP 

  PENCET MACRO P1
      PUSH BX
      MOV BX,[P1]             
      MOV [TONE],BX          
      POP BX
      CALL SOUNDER          
  ENDM 

